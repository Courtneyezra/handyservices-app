// ---------------------------------------------------------------------------
// Comparison: Old EVE vs New Contextual
// ---------------------------------------------------------------------------

/** Side-by-side comparison of EVE and contextual pricing for the same job */
export interface PricingComparisonResult {
  /** Old EVE engine output */
  eve: {
    pricePence: number;
    segment: string;
    valueMultiplier: number;
    hourlyRatePence: number;
  };
  /** New contextual engine output */
  contextual: ContextualPricingResult;
  /** Difference between the two engines */
  delta: {
    /** Absolute difference in pence (contextual minus EVE) */
    pence: number;
    /** Percentage difference */
    percent: number;
    /** Whether contextual is higher, lower, or the same */
    direction: 'higher' | 'lower' | 'same';
  };
  /** The input context used for both engines */
  inputContext: PricingContext;
  /** ISO 8601 timestamp of when the comparison was run */
  timestamp: string;
}

