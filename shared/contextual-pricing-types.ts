// ---------------------------------------------------------------------------
// Layout & Booking Types
// ---------------------------------------------------------------------------

/** Recommended quote page layout complexity */
export type LayoutTier = 'quick' | 'standard' | 'complex';

/**
 * Deterministic layout tier based on line item count.
 *   1 line   → 'quick'
 *   2-3 lines → 'standard'
 *   4+ lines  → 'complex'
 */
export function getLayoutTier(lineCount: number): LayoutTier {
  if (lineCount <= 1) return 'quick';
  if (lineCount <= 3) return 'standard';
  return 'complex';
}

/** Available booking mode options shown on the quote page */
export type BookingMode = 'standard_date' | 'flexible_discount' | 'urgent_premium' | 'deposit_split';

// ---------------------------------------------------------------------------
// Quote Messaging (LLM-generated customer-facing content)
// ---------------------------------------------------------------------------

/** Customer-facing messaging generated by the LLM alongside pricing */
export interface QuoteMessaging {
  /** Customer-facing headline, e.g. "Your Kitchen Sorted" */
  contextualHeadline: string;
  /** 1-2 sentence summary for the customer */
  contextualMessage: string;
  /** 3-7 word polished job summary for the hero, e.g. "Dripping tap sorted for good" */
  jobTopLine?: string;
  /** AI-generated scope-of-work summary (2-4 sentences, 40-80 words) */
  proposalSummary: string;
  /** 3-5 approved claim bullets for the quote page */
  valueBullets: string[];
  /** Exactly 2 value lines for WhatsApp message */
  whatsappValueLines: string[];
  /** Closing line for WhatsApp message */
  whatsappClosing: string;
  /** Recommended layout: quick/standard/complex */
  layoutTier: LayoutTier;
  /** Which booking options to show on the quote page (deterministic, not LLM) */
  bookingModes: BookingMode[];
  /** True if AI parser fallback was triggered */
  requiresHumanReview: boolean;
  /** Why human review is needed (only set when requiresHumanReview is true) */
  reviewReason?: string;
  /**
   * Dead zone framing note shown near the price when the quote lands in the
   * £100-£200 band (0% conversion in analytics). E.g. "That's around £5 a
   * day for 30 days of not having to think about this."
   */
  deadZoneFraming?: string;
}

// ---------------------------------------------------------------------------
// Category Rate (used by reference price lookup)
