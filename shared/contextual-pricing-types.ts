/**
 * Contextual Pricing Engine — Shared Types
 *
 * All TypeScript interfaces shared between server and client for the
 * contextual pricing engine. This engine replaces the multiplier-based
 * EVE approach with a layered system:
 *
 *   Layer 1: Reference rates (market-grounded hourly rates per job category)
 *   Layer 3: LLM contextual pricing (adjusts based on full context)
 *   Layer 4: Guardrails (floor/ceiling/margin checks)
 *
 * Segment type is intentionally kept as `string` here to avoid
 * duplicating the LeadSegment type from schema.ts — callers should
 * use LeadSegment from schema.ts when constructing PricingContext.
 */

